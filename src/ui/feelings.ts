export function pickFeeling(category: string, n?: number): string | null {
  const pool = FEELINGS[category] ?? FEELINGS['generic'];
  if (!pool || pool.length === 0) return null;
  const line = pool[Math.floor(Math.random() * pool.length)];
  return n !== undefined ? line.replace('{n}', String(n)) : line;
}
